#include "../include/libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	unsigned int	i;
	int				src_length;

	i = 0;
	src_length = ft_strlen(src);
	if (size > 0)
	{
		while (src[i] && (i < (size - 1)))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = 0;
	}
	return (src_length);
}
